        });
    });

    function ajax_search() {

        var judul = $("#search_query").val();
        $.ajax({
            url: "<?= base_url('lembaga/cari') ?>",
            data: "judul=" + judul,
            success: function(data) {
                // jika data sukses diambil dari server, tampilkan di <select id=kota>
                $("#display_results").html(data);
            }
        });

    }
</script>

